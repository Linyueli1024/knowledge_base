const MobileToolbarContent = ({
  type,
  onBack,
}: {
  type: "highlighter" | "link"
  onBack: () => void
}) => (
  <>
    <ToolbarGroup>
      <Button variant="ghost" onClick={onBack}>
        <ArrowLeftIcon className="tiptap-button-icon" />
        {type === "highlighter" ? (
          <HighlighterIcon className="tiptap-button-icon" />
        ) : (
          <LinkIcon className="tiptap-button-icon" />
        )}
      </Button>
    </ToolbarGroup>

    <ToolbarSeparator />

    {type === "highlighter" ? (
      <ColorHighlightPopoverContent />
    ) : (
      <LinkContent />
    )}
  </>
)

export function SimpleEditor({
  initialMarkdown = "",
  documentId = "default",
  noteRelativePath,
  vaultPath,
  editable = true,
  onMarkdownChange,
}: {
  initialMarkdown?: string
  documentId?: string
  noteRelativePath: string
  vaultPath: string
  editable?: boolean
  onMarkdownChange?: (markdown: string) => void
}) {
  const isMobile = useIsBreakpoint()
  const { height } = useWindowSize()
  const [mobileView, setMobileView] = useState<"main" | "highlighter" | "link">(
    "main"
  )
  const toolbarRef = useRef<HTMLDivElement>(null)
  const markdownSyncTimerRef = useRef<number | null>(null)

  const flushMarkdownChange = (currentEditor: Editor) => {
    onMarkdownChange?.(tiptapToMarkdown(currentEditor))
  }

  const uploadImage = async (
    file: File,
    onProgress?: (event: { progress: number }) => void,
    abortSignal?: AbortSignal,
  ) => {
    const { src, localSrc } = await createVaultImageUpload(
      file,
      {
        vaultPath,
        noteRelativePath,
      },
      onProgress,
      abortSignal,
    )

    return JSON.stringify({ src, localSrc })
  }

  const editor = useEditor({
    immediatelyRender: false,
    shouldRerenderOnTransaction: false,
    editorProps: {
      attributes: {
        autocomplete: "off",
        autocorrect: "off",
        autocapitalize: "off",
        "aria-label": "Main content area, start typing to enter text.",
        class: "simple-editor",
      },
    },
    content: markdownToTiptap(initialMarkdown, {
      vaultPath,
      noteRelativePath,
    }),
    editable,
    extensions: [
      StarterKit.configure({
        codeBlock: false,
        horizontalRule: false,
        link: {
          openOnClick: false,
          enableClickSelection: true,
        },
      }),
      CodeBlockHighlight.configure({
        lowlight,
        defaultLanguage: "plaintext",
      }),
      HorizontalRule,
      TextAlign.configure({ types: ["heading", "paragraph"] }),
      TaskList,
      TaskItem.configure({ nested: true }),
      Highlight.configure({ multicolor: true }),
      VaultImage.configure({
        allowBase64: true,
      }),
      Typography,
      Superscript,
      Subscript,
      Selection,
      ImageUploadNode.configure({
        accept: "image/*",
        maxSize: MAX_FILE_SIZE,
        limit: 3,
        upload: uploadImage,
        onError: (error) => console.error("Upload failed:", error),
      }),
    ],
    onUpdate: ({ editor: currentEditor }) => {
      if (!onMarkdownChange) return

      if (markdownSyncTimerRef.current !== null) {
        window.clearTimeout(markdownSyncTimerRef.current)
      }

      markdownSyncTimerRef.current = window.setTimeout(() => {
        markdownSyncTimerRef.current = null
        flushMarkdownChange(currentEditor as Editor)
      }, 300)
    },
  })

  const rect = useCursorVisibility({
    editor,
    overlayHeight: toolbarRef.current?.getBoundingClientRect().height ?? 0,
  })

  useEffect(() => {
    if (!isMobile && mobileView !== "main") {
      setMobileView("main")
    }
  }, [isMobile, mobileView])

  useEffect(() => {
    if (!editor) return
    editor.setEditable(editable)
  }, [editor, editable])

  useEffect(() => {
    return () => {
      if (markdownSyncTimerRef.current !== null) {
        window.clearTimeout(markdownSyncTimerRef.current)
        markdownSyncTimerRef.current = null
      }

      if (editor && onMarkdownChange) {
        flushMarkdownChange(editor)
      }
    }
  }, [editor, onMarkdownChange])

  useEffect(() => {
    if (!editor || !onMarkdownChange) return

    const handleBlur = () => {
      if (markdownSyncTimerRef.current !== null) {
        window.clearTimeout(markdownSyncTimerRef.current)
        markdownSyncTimerRef.current = null
      }

      flushMarkdownChange(editor)
    }

    editor.on("blur", handleBlur)

    return () => {
      editor.off("blur", handleBlur)
    }
  }, [editor, onMarkdownChange])

  return (
    <div className="simple-editor-wrapper" data-document-id={documentId}>
      <EditorContext.Provider value={{ editor }}>
        <Toolbar
          ref={toolbarRef}
          style={{
            ...(isMobile
              ? {
                  bottom: `calc(100% - ${height - rect.y}px)`,
                }
              : {}),
          }}
        >
          {mobileView === "main" ? (
            <MainToolbarContent
              onHighlighterClick={() => setMobileView("highlighter")}
              onLinkClick={() => setMobileView("link")}
              isMobile={isMobile}
            />
          ) : (
            <MobileToolbarContent
              type={mobileView === "highlighter" ? "highlighter" : "link"}
              onBack={() => setMobileView("main")}
            />
          )}
        </Toolbar>

        <div className="simple-editor-scroll">
          <EditorContent
            editor={editor}
            role="presentation"
            className="simple-editor-content"
          />
        </div>
      </EditorContext.Provider>
    </div>
  )
}
